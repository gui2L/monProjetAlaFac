public class Artefact {
    private String element;
    private int x, y;

    public Artefact(String element, int x, int y){
        this.element = element;
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
